/**
 * 将模板 patch 拼接到基础 prompt 末尾
 */
function getPromptWithTemplate(basePrompt, patch) {
  if (!patch) return basePrompt;
  return basePrompt + "\n\n" + patch;
}

function getTemplateList() {
  return Object.keys(PRD_TEMPLATES);
}

function getTemplateLabel(key) {
  return PRD_TEMPLATES[key]?.label || key;
}

function getTemplatePatch(key) {
  const tpl = PRD_TEMPLATES[key] || PRD_TEMPLATES.general;
  return {
    analyzePatch: tpl.analyzePatch || "",
    prdPatch: tpl.prdPatch || "",
    reviewPatch: tpl.reviewPatch || "",
  };
}
